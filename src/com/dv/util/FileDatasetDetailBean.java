package com.dv.util;

public class FileDatasetDetailBean {

    private String colname;
    private String coltype;
    private int missingnum;
    private String missingratio;

    public String getColname() {
        return colname;
    }

    public void setColname(String colname) {
        this.colname = colname;
    }

    public String getColtype() {
        return coltype;
    }

    public void setColtype(String coltype) {
        this.coltype = coltype;
    }

    public int getMissingnum() {
        return missingnum;
    }

    public void setMissingnum(int missingnum) {
        this.missingnum = missingnum;
    }

    public String getMissingratio() {
        return missingratio;
    }

    public void setMissingratio(String missingratio) {
        this.missingratio = missingratio;
    }


}
